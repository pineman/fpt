#[derive(Clone)]
pub struct Instruction {
    pub opcode: u8,
    pub mnemonic: String,
    pub size: u8,
    pub cycles: u8,
    pub cycles_not_taken: u8,
}

impl Instruction {
    // Arguments in the same order as https://www.pastraiser.com/cpu/gameboy/gameboy_opcodes.html
    fn new(opcode: u8, mnemonic: &str, size: u8, cycles: u8, cycles_not_taken: u8) -> Self {
        let mnemonic = mnemonic.to_string();
        Self {
            opcode,
            mnemonic,
            size,
            cycles,
            cycles_not_taken,
        }
    }
}

